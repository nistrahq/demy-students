import Foundation

enum AppSpacing {
    /// Extra small – use for tight spaces or minor adjustments.
    static let xs: CGFloat = 4

    /// Small – use for minor separations between elements.
    static let sm: CGFloat = 8

    /// Medium – use for standard separations between elements.
    static let md: CGFloat = 16

    /// Large – use for more significant separations.
    static let lg: CGFloat = 24

    /// Extra large – use for wide separations or padding.
    static let xl: CGFloat = 32

    /// XXL – use for very wide separations or padding.
    static let xxl: CGFloat = 40
}
