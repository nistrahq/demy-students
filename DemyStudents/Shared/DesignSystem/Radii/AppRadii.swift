import Foundation

enum AppRadii {
    /// Ultra small — for subtle borders and small elements.
    static let xs: CGFloat = 4

    /// Small — for input fields, small buttons.
    static let sm: CGFloat = 8

    /// Medium — for cards, modals, buttons.
    static let md: CGFloat = 12

     /// Large — for cards, containers, and larger elements.
    static let lg: CGFloat = 16

    /// Extra Large — for modals, main panels, and prominent elements.
    static let xl: CGFloat = 20

    /// Full — for pill buttons and fully rounded elements.
    static let full: CGFloat = 999
}
